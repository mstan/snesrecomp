"""Recomp-side runner for Phase B differential fuzz.

Pipeline:
  1. Read snippets/snippets.json (from generate_snippets.py).
  2. For each snippet, decode its ROM bytes and call emit_function
     to produce a C body.
  3. Assemble all bodies into a single C source file, plus a minimal
     harness that seeds CPU state, runs each snippet function, and
     prints final state as JSONL to stdout.
  4. Compile once with cl.exe.
  5. Run; capture stdout; write to results/recomp_final.jsonl.

Final state dump per snippet:
  {"id": ..., "A": ..., "X": ..., "Y": ..., "P": ..., "D": ...,
   "wram_delta": {"0x0010": 0x34, ...}, "error": null}

Only WRAM addresses that changed vs the seeded initial state are
reported, keeping the output lean.

Design decision: generate WITHOUT --reverse-debug so we get plain
g_ram[] accesses — the fuzz harness can't (and shouldn't) drag in
the whole RDB_LOAD/STORE/INSN_HOOK macrology.
"""
from __future__ import annotations
