"""Append exclude_range proposals from smwdisx_exclude_pass.py into the
target bank cfg, just after the last existing `exclude_range` line.
Leaves comments so the source of each addition is traceable.

Usage:
  python tools/smwdisx_apply_excludes.py <bank_hex>
"""

import os
import subprocess
import sys

CFG_DIR = os.path.normpath(os.path.join(
    os.path.dirname(__file__), '..', '..', 'snesrecomp-v2', 'tools', 'recomp'))
PASS = os.path.join(os.path.dirname(__file__), 'smwdisx_exclude_pass.py')


def main():
    if len(sys.argv) != 2:
        print(__doc__)
        sys.exit(2)
    bank = sys.argv[1]
    cfg_path = os.path.join(CFG_DIR, f'bank{bank.lower()}.cfg')
    if not os.path.isfile(cfg_path):
        print(f'No cfg: {cfg_path}')
        sys.exit(1)

    out = subprocess.run([sys.executable, PASS, bank],
                         capture_output=True, text=True).stdout
    proposal_lines = [ln for ln in out.splitlines()
                      if ln.startswith('exclude_range ')]
    if not proposal_lines:
        print(f'bank {bank}: nothing to apply')
        return

    with open(cfg_path, 'r', encoding='utf-8') as f:
        cfg_lines = f.readlines()

    # Find last existing exclude_range line; insert after it.
    last_idx = None
    for i, ln in enumerate(cfg_lines):
        if ln.lstrip().startswith('exclude_range'):
            last_idx = i
    if last_idx is None:
        # Insert near top after 'bank =' and comments.
        for i, ln in enumerate(cfg_lines):
            if ln.startswith('bank'):
                last_idx = i
                break
        else:
            last_idx = 0

    header = ('\n# --- SMWDisX DATA_ import (auto-generated by '
              'tools/smwdisx_exclude_pass.py) ---\n')
    new_block = [header] + [ln + '\n' for ln in proposal_lines] + [
        '# --- end SMWDisX DATA_ import ---\n']
    cfg_lines[last_idx + 1:last_idx + 1] = new_block

    with open(cfg_path, 'w', encoding='utf-8') as f:
        f.writelines(cfg_lines)
    print(f'bank {bank}: applied {len(proposal_lines)} exclude_range entries to {cfg_path}')


if __name__ == '__main__':
    main()
